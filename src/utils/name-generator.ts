const nouns = [
  "potato",
  "airplane",
  "dog",
  "cat",
  "cactus",
  "pear",
  "capybara",
];

export function generateRandomUsername(): string {
  const adjective = adjectives[~~(Math.random() * (adjectives.length - 1))];
  const verb = verbs[~~(Math.random() * (verbs.length - 1))];
  const noun = nouns[~~(Math.random() * (nouns.length - 1))];
  const number = Math.round(~~(Math.random() * 100));

  return `${adjective}-${verb}-${noun}-${number}`;
}
